// Components use props to communicate with each other
// Every parent component can pass some info to its child components with props
import { getImageUrl } from './utils.js';

// unpacking the props
function Avatar({person, size}) { 
  return (
    <img
      className='avatar rounded-[50%]'
      src={getImageUrl(person)}
      alt={person.name}
      width={size}
      height={size}
    />
  );
}

